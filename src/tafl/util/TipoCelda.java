package tafl.util;

/**
 * La clase TipoCelda representa los tipos de una celda, pudiendo ser TRONO, PROVINCIA o NORMAL.
 * 
 * @author Pablo Citores
 * @author David Santamaria
 * @since 1.0
 * @version 1.0
 */

public enum TipoCelda {
	/**
     * Tipo de celda correspondiente al trono.
     */
	TRONO,
    
	/**
     * Tipo de celda correspondiente a una provincia.
     */
	PROVINCIA,
	/**
     * Tipo de celda normal.
     */
	NORMAL
}